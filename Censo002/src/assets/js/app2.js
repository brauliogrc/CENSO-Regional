function mostrarbusq() {
    document.getElementById('ModalContenidoBusq').style.pointerEvents = 'all';
    document.getElementById('ModalContenidoBusq').style.opacity = 1;
}
function cerrarbusq() {
    document.getElementById('ModalContenidoBusq').style.pointerEvents = 'none';
    document.getElementById('ModalContenidoBusq').style.opacity = 0;
}